function onTick()
	CV=input.getNumber(3) MA=input.getNumber(4)
	bound=input.getBool(1)
	local info0=unpk(input.getNumber(1))
	local info2=unpk(input.getNumber(2))

	local ss,sc,ec,cc=true,0,0,0
    for i=1,6 do
        local CE = (info0 & (1 << (i - 1))) ~= 0
        local DC = (info2 & (1 << (i + 15))) ~= 0
		if CE then cc=cc+1 end
		if DC then
			if ss then sc=sc+1 end
			ec=ec+1
		elseif CE then
			ss=false
			ec=0
		end
	end
	
	Closed=false
	if     bound and (ec==(cc-1)) then Closed=true end
	if not bound and (sc==(cc-1)) then Closed=true end
end
